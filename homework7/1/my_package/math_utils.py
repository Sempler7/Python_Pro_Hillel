"""Модуль з функціями, які обчислюють факторіал числа,
 а також функція для знаходження найбільшого спільного
  дільника двох чисел
  """


def factorial(n: int) -> int:
    """Вираховує факторіал числа через цикл"""
    result = 1
    for i in range(2, n + 1):
        result *= i
    return result


def gcd(a: int, b: int) -> int:
    """Знаходить найбільший спільний дільник (gcd) двох чисел."""
    while b != 0:
        a, b = b, a % b
    return a
