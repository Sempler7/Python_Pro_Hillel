"""Модуль з функціями для перетворення тексту в верхній регістр
 та видалення пробілів на початку та в кінці рядка
 """


def upper_case(s: str) -> str:
    """Конвертація строки в верхній регістр"""
    return s.upper()


def del_spaces(s: str) -> str:
    """Видалення пробілів на початку та в кінці рядка."""
    return s.strip()
